<?php
// operator
$a = 7;
$b = 3;

echo "Operator + : " . ($a + $b) . "\n"; //penjumlahan
echo "Operator - : " . ($a - $b) . "\n"; //pengurangan
echo "Operator * : " . ($a * $b) . "\n"; //perkalian
echo "Operator ** : " . ($a ** $b) . "\n"; //perpangkatan
echo "Operator % : " . ($a % $b) . "\n"; //Modulus / sisa bagi
echo "Operator / : " . ($a / $b) . "\n"; //pembagian